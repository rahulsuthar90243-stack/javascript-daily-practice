// Arithmetic Operators
let a = 10;
let b = 10;
console.log("Addition:", a + b); // 20
console.log("Subtraction:", a - b); // 0
console.log("Multiplication:", a * b); // 100
console.log("Division:", a / b); // 1
console.log("Modulus:", a % b); // 0

// Assignment Operators
let c = 5;
c = 10; // Assignment
console.log("Assignment:",c ); // 10
c += 5;
console.log("Addition Assignment:", c); // 15
c -= 3;
console.log("Subtraction Assignment:", c); // 12
c *= 2;
console.log("Multiplication Assignment:", c); // 24
c /= 4;
console.log("Division Assignment:", c); // 6
c %= 4;
console.log("Modulus Assignment:", c); // 2