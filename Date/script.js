// Date Object
let date = new Date();
console.log(date);

// Get Full Year
let year = date.getFullYear();
console.log("Year:", year);

// Get Times
console.log("Hours:", date.getTime());

//Get Day
console.log("Day:", date.getDay());

// Get Month
console.log("Month:", date.getMonth() + 1); // Months are zero-based